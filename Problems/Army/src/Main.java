class Army {

    public static void createArmy() {
        Unit james = new Unit("james");
        Unit harry = new Unit("harry");
        Unit lily = new Unit("lily");
        Unit sirius = new Unit("sirius");
        Unit timothy = new Unit("timothy");

        Knight igris = new Knight("igris");
        Knight tusk = new Knight("tusk");
        Knight iron = new Knight("iron");

        General jinwoo = new General("jinwoo");

        Doctor jamie = new Doctor("jamie");
    }


    // Don't change the code below
    static class Unit {
        static String nameUnit;
        static int countUnit;

        public Unit(String name) {
            countUnit++;
            nameUnit = name;

        }
    }

    static class Knight {
        static String nameKnight;
        static int countKnight;

        public Knight(String name) {
            countKnight++;
            nameKnight = name;

        }
    }

    static class General {
        static String nameGeneral;
        static int countGeneral;

        public General(String name) {
            countGeneral++;
            nameGeneral = name;

        }
    }

    static class Doctor {
        static String nameDoctor;
        static int countDoctor;

        public Doctor(String name) {
            countDoctor++;
            nameDoctor = name;

        }
    }

    public static void main(String[] args) {
        createArmy();
        System.out.println(Unit.countUnit);
        System.out.println(Knight.countKnight);
        System.out.println(General.countGeneral);
        System.out.println(Doctor.countDoctor);
    }

}